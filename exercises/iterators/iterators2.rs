// Step 1.
// Complete the `capitalize_first` function.
// "hello" -> "Hello"
pub fn capitalize_first(input: &str) -> String {
    let mut c = input.chars();
    match c.next() {
        None => String::new(),
        Some(first) => first.to_uppercase().to_string() + c.as_str()
        //Some(first) => {
            //let mut letter = first.to_uppercase().to_string();
            //letter.extend(c);
            //letter
        //}
    }
}

